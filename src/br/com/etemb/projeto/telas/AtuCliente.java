    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        try {
            int id = Integer.parseInt(campo_id.getText());
            Conexao conecta_cliente = new Conexao();
            conecta_cliente.RemoverCliente(id);
            campo_id.setText("");
            campo_nome.setText("");
            campo_telefone.setText("");
            campo_celular.setText("");
            campo_email.setText("");
            JOptionPane.showMessageDialog(null, "Dados removidos com sucesso!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        }
        catch (Exception ex)  {
            JOptionPane.showMessageDialog(null, "Erro no Banco de Dados - " + ex.getMessage(), "Aviso", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

