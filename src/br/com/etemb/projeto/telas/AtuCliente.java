    private void botaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoBuscarActionPerformed
        try {
            Clientes clientes = new Clientes();
            clientes.setId(Integer.parseInt(campo_id.getText()));
            Conexao conexao = new Conexao();
            clientes = conexao.ProcurarCliente(clientes.getId());
                if(clientes.getId() != 0) {
                    campo_nome.setText(clientes.getNome());
                    campo_telefone.setText(clientes.getTelefone());
                    campo_email.setText(clientes.getEmail());
                    campo_celular.setText(clientes.getCelular());
                    botaoAlterar.setEnabled(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Cliente n√£o encontrado!!", "Aviso", JOptionPane.ERROR_MESSAGE);
                    campo_id.setText("");
                    campo_nome.setText("");
                    campo_telefone.setText("");
                    campo_celular.setText("");
                    campo_email.setText("");
                }
        }
        catch(Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro no Banco de Dados - " + ex.getMessage(), "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botaoBuscarActionPerformed

    private void botaoAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarActionPerformed
        try {
            int id = Integer.parseInt(campo_id.getText());
            String nome = campo_nome.getText();
            String telefone = campo_telefone.getText();
            String celular = campo_celular.getText();
            String email = campo_email.getText();
                Clientes clientes = new Clientes(nome, telefone, celular, email);
                Conexao conecta_cliente = new Conexao();
                conecta_cliente.AtualizarCliente(clientes, id);
            JOptionPane.showMessageDialog(null, "Dados atualizados com sucesso!!", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            campo_id.setEnabled(true);
            campo_nome.setEnabled(false);
            campo_telefone.setEnabled(false);
            campo_celular.setEnabled(false);
            campo_email.setEnabled(false);
            botaoAtualizar.setEnabled(false);
            botaoExcluir.setEnabled(false);
            botaoCancelar.setEnabled(false);
        }
        catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro no Banco de Dados - " + ex.getMessage(), "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botaoAtualizarActionPerformed

